/**
 * An Contact
 *
 * @author gregory.jo
 * @version 1.0
 * @updated 29-Apr-2014 16:17:00
 */
class Contact extends AbstractEntity
{
    /**
     * @var string The address of the contact
     **/
    protected $address = '';

    /**
     * @var string The email of the contact
     **/
    protected $email = '';

    /**
     * @var string The fax number of the contact
     **/
    protected $fax = '';

    /**
     * @var string The name of the contact
     * @validate StringLength(minimum = 2, maximum = 80)
     **/
    protected $name;

    /**
     * @var string The telephone number of the contact
     **/
    protected $phone = '';

    /**
     * @var string The role of the contact
     **/
    protected $role = '';

    /**
     * @var string The url of the homepage of the contact
     **/
    protected $url = '';

    public function __construct($name = NULL) {
        $this->setName($name);
    }

    /**
     * @param string $address
     */
    public function setAddress($address)
    {
        $this->address = $address;
    }

    /**
     * @return string
     */
    public function getAddress()
    {
        return $this->address;
    }

    /**
     * @param string $email
     */
    public function setEmail($email)
    {
        $this->email = $email;
    }

    /**
     * @return string
     */
    public function getEmail()
    {
        return $this->email;
    }

    /**
     * @param string $fax
     */
    public function setFax($fax)
    {
        $this->fax = $fax;
    }

    /**
     * @return string
     */
    public function getFax()
    {
        return $this->fax;
    }

    /**
     * @param string $name
     */
    public function setName($name)
    {
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param string $phone
     */
    public function setPhone($phone)
    {
        $this->phone = $phone;
    }

    /**
     * @return string
     */
    public function getPhone()
    {
        return $this->phone;
    }

    /**
     * @param string $role
     */
    public function setRole($role)
    {
        $this->role = $role;
    }

    /**
     * @return string
     */
    public function getRole()
    {
        return $this->role;
    }

    /**
     * @param string $url
     */
    public function setUrl($url)
    {
        $this->url = $url;
    }

    /**
     * @return string
     */
    public function getUrl()
    {
        return $this->url;
    }


}